using UnityEngine;

public class PickupRotator : MonoBehaviour
{
	// ------------------------------------------------------------------------------------------------------------------------------
	// [Code - private]
	private Rigidbody _rigidBody;
	// ------------------------------------------------------------------------------------------------------------------------------
	void Start()
	{
		_rigidBody = GetComponent<Rigidbody>();
	}
	// ------------------------------------------------------------------------------------------------------------------------------
	void Update()
	{
		_rigidBody.AddTorque(Vector3.up);
	}
	// ------------------------------------------------------------------------------------------------------------------------------
}
